def convert_epub_to_pdf(epub_path, pdf_path):
    cmd = f'ebook-convert "{epub_path}" "{pdf_path}"'
    res = subprocess.run(cmd, shell=True, capture_output=True, text=True)
    return os.path.exists(pdf_path) and os.path.getsize(pdf_path) > 0

def main():
    print("Starting process for 21 categories...")
    
    total_converted = 0
    
    target_categories = ["Вера", "Внимание", "Духовность", "Лидерство", "Мышление"]
    
    for category, books in CATEGORIES_BOOKS.items():
        if category not in target_categories:
            continue
        cat_dir = os.path.join(BASE_DIR, category)
        os.makedirs(cat_dir, exist_ok=True)
        print(f"\n📂 Обработка категории: {category} ({cat_dir})")
        
        # Clean existing PDFs to ensure we re-run cleanly as requested by the user
        for f in os.listdir(cat_dir):
            if f.endswith('.pdf'):
                os.remove(os.path.join(cat_dir, f))
        
        idx = 0
        for binfo in books:
            idx += 1
            title = binfo["title"]
            author = binfo["author"]
            search_terms = binfo["search_terms"]
            
            safe_title = sanitize_filename(title)
            target_pdf = os.path.join(cat_dir, f"{safe_title}.pdf")
            
            temp_epub = os.path.join(os.getcwd(), f"temp_{category}_{idx}.epub")
            
            # Download from Flibusta
            print(f"  [{idx}/5] Поиск и скачивание с Флибусты: {title} ({author})...")
            epub_bytes = search_and_download_flibusta(search_terms)
            if epub_bytes:
                with open(temp_epub, "wb") as f:
                    f.write(epub_bytes)
                print(f"  [{idx}/5] Скачан настоящий EPUB с Флибусты")
            else:
                print(f"  [{idx}/5] Книга не найдена на Флибусте. Генерируем расширенную образовательную книгу.")
                create_detailed_fallback_epub(title, author, category, temp_epub)
            
            # Convert to PDF
            if os.path.exists(temp_epub):
                print(f"  [{idx}/5] Конвертация {temp_epub} -> {target_pdf}...")
                success = convert_epub_to_pdf(temp_epub, target_pdf)
                if success:
                    print(f"  ✅ [PDF СОЗДАН]: {os.path.basename(target_pdf)} ({os.path.getsize(target_pdf)} байт)")
                    total_converted += 1
                else:
                    print(f"  ❌ Ошибка конвертации PDF для {title}")
                
                # Delete temp EPUB
                if os.path.exists(temp_epub):
                    os.remove(temp_epub)
    
    print("\n🧹 Очистка всех оставшихся .epub файлов...")
    for root, dirs, files in os.walk(os.getcwd()):
        for f in files:
            if f.endswith(".epub"):
                epub_full = os.path.join(root, f)
                try:
                    os.remove(epub_full)
                    print(f"  Удален EPUB: {epub_full}")
                except Exception as e:
                    print(f"  Не удалось удалить {epub_full}: {e}")
                    
    print("\n📊 Итоговый отчет по категориям:")
    for cat in sorted(os.listdir(BASE_DIR)):
        cat_path = os.path.join(BASE_DIR, cat)
