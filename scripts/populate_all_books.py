def sanitize_filename(name):
    clean = re.sub(r'[\\/*?:"<>|]', "", name)
    clean = clean.replace(" ", "_").lower()
    return clean[:60]

def search_and_download_flibusta(search_terms):
    for term in search_terms:
        try:
            print(f"    Пробуем запрос к Флибусте: '{term}'...")
            url = f"https://flibusta.is/opds/search?searchType=books&searchTerm={urllib.parse.quote(term)}"
            req = urllib.request.Request(url, headers={'User-Agent': 'Mozilla/5.0 (Windows NT 10.0; Win64; x64)'})
            with urllib.request.urlopen(req, timeout=10) as resp:
                data = resp.read()
                tree = ET.fromstring(data)
                entries = tree.findall('{http://www.w3.org/2005/Atom}entry')
                for entry in entries[:5]:
                    links = entry.findall('{http://www.w3.org/2005/Atom}link')
                    for l in links:
                        ltype = l.attrib.get('type', '')
                        lhref = l.attrib.get('href', '')
                        if 'application/epub+zip' in ltype or lhref.endswith('/epub'):
                            epub_url = lhref if lhref.startswith('http') else f"https://flibusta.is{lhref}"
                            print(f"      Найдена ссылка на EPUB: {epub_url}. Скачиваем...")
                            epub_req = urllib.request.Request(epub_url, headers={'User-Agent': 'Mozilla/5.0'})
                            with urllib.request.urlopen(epub_req, timeout=15) as epub_resp:
                                epub_data = epub_resp.read()
                                if len(epub_data) > 10000 and epub_data.startswith(b'PK'):
                                    print(f"      ✅ Успешно скачано {len(epub_data)} байт.")
                                    return epub_data
        except Exception as e:
            print(f"      Ошибка при поиске/скачивании '{term}': {e}")
    return None

